<?php

namespace App\Repository;

use App\DTO\UserDto;
use App\Entity\User;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<User>
 *
 * @method User|null find($id, $lockMode = null, $lockVersion = null)
 * @method User|null findOneBy(array $criteria, array $orderBy = null)
 * @method User[]    findAll()
 * @method User[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class UserRepository extends ServiceEntityRepository
{

    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, User::class);
    }

    public function save(User $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(User $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function create(UserDto $dto): User
    {
        $user = new User();
        $user->setEmail($dto->email);
        $user->setName($dto->name);
        $user->setGoogleId($dto->google_id);
        $user->setProfileImage($dto->profile_image);
        $user->setPassword($dto->password);

        $this->save($user, true);

        return $user;
    }

    public function update(UserDto $userDto, User $user): User
    {
        $user->setName($userDto->name);
        $user->setProfileImage($userDto->profile_image);
        $user->setGoogleId($userDto->google_id);
        $user->setEmail($userDto->email);

        $this->save($user, true);

        return $user;
    }

    public function delete(User $user)
    {
        $this->remove($user, true);
    }



